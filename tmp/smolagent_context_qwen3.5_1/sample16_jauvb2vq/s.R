library(haven)
library(dplyr)
library(readr)
w1<-read_delim('data/input/wave_one_lsype_family_background_2020.tab',delim='	',show_col_types=FALSE)
w2<-read_delim('data/input/wave_two_lsype_family_background_2020.tab',delim='	',show_col_types=FALSE)
w3<-read_delim('data/input/wave_three_lsype_family_background_2020.tab',delim='	',show_col_types=FALSE)
w4<-read_delim('data/input/wave_four_lsype_family_background_2020.tab',delim='	',show_col_types=FALSE)
d<-full_join(full_join(full_join(w1,w2,by='NSID'),w3,by='NSID'),w4,by='NSID')
r12<-function(x){case_when(is.na(x)~-3,x==-999~-2,x==-992~-9,x==-99~-3,x==-94~-8,x==-92~-9,x==-91~-1,x==-3~-3,x==-1~-8,TRUE~as.numeric(x))}
r34<-function(x){case_when(is.na(x)~-3,x==-996~-1,x==-99~-3,x==-92~-9,x==-1~-8,TRUE~as.numeric(x))}
d<-mutate(d,W1r=r12(W1GrsswkHH),W2r=r12(W2GrsswkHH),W3r=r34(W3incestw),W4r=r34(w4IncEstW))
d<-mutate(d,i14r=case_when(W1r<0~W1r,W1r<=49~1,W1r<=99~2,W1r<=199~3,W1r<=299~4,W1r<=399~5,W1r<=499~6,W1r<=599~7,W1r<=699~8,W1r<=799~9,W1r<=899~10,W1r<=999~11,W1r>=1000~12))
d<-mutate(d,i15r=case_when(W2r<0~W2r,W2r<=49~1,W2r<=99~2,W2r<=199~3,W2r<=299~4,W2r<=399~5,W2r<=499~6,W2r<=599~7,W2r<=699~8,W2r<=799~9,W2r<=899~10,W2r<=999~11,W2r>=1000~12))
alvl<-c(-9,-8,-3,-2,-1,1:12)
albl<-c('R','DK','NA','SE','NAP','B1','B2','B3','B4','B5','B6','B7','B8','B9','B10','B11','B12')
d$incwhh14<-factor(d$i14r,levels=alvl,labels=albl)
d$incwhh15<-factor(d$i15r,levels=alvl,labels=albl)
d$incwhh16<-factor(d$W3r,levels=alvl,labels=albl)
d$incwhh17<-factor(d$W4r,levels=alvl,labels=albl)
d<-select(d,NSID,incwhh14,incwhh15,incwhh16,incwhh17,W1r,W2r)
d<-rename(d,incwhhcnt14=W1r,incwhhcnt15=W2r)
d<-select(d,NSID,incwhh14,incwhh15,incwhhcnt14,incwhhcnt15,incwhh16,incwhh17)
dir.create('data/output',showWarnings=FALSE,recursive=TRUE)
write_csv(d,'data/output/cleaned_data.csv')
cat('Done')
